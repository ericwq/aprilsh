package terminal

const (
	DISPATCH_ESCAPE = iota + 1
	DISPATCH_CSI
	DISPATCH_CONTROL
)

type Emulator interface {
	Print(act Action)
	Execute(act Action)
	Dispatch() *Dispatcher
	CSIdispatch(act Action)
	ESCdispatch(act Action)
	OSCend(act Action)
	Resize(width int, height int)
	User() *UserInput
	Framebuffer() *Framebuffer
}

type Action interface {
	ActOn(t Emulator)
	Ignore() bool
	Name() string
	SetChar(rune)
	SetPresent(bool)
	GetChar() rune
	IsPresent() bool
}

